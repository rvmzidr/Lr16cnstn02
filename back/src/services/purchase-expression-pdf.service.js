async function buildPurchaseExpressionPdf(data) {
  return new Promise((resolve, reject) => {
    const language = normalizeLanguage(data.language);
    const labels = LABELS[language];
    const doc = new PDFDocument({
      size: "A4",
      margins: { top: 30, right: 24, bottom: 24, left: 24 },
      info: {
        Title: `Expression de besoins ${data.reference || ""}`.trim(),
        Author: "Plateforme LR16-CNSTN-02",
      },
    });

    const fonts = registerFonts(doc);
    const chunks = [];
    doc.on("data", (chunk) => chunks.push(chunk));
    doc.on("end", () => resolve(Buffer.concat(chunks)));
    doc.on("error", reject);

    const pageWidth = doc.page.width;
    const bottomLimit = doc.page.height - doc.page.margins.bottom;
    const availableWidth = pageWidth - doc.page.margins.left - doc.page.margins.right;
    const tableWidth = Math.min(OFFICIAL_TABLE_WIDTH, availableWidth);
    const gridWidths = buildGridWidths(tableWidth);
    const left = (pageWidth - tableWidth) / 2;
    const top = doc.page.margins.top;

    let y = top;
    y = drawIdentityRow(doc, fonts, labels, left, y, gridWidths, data, language);
    y = drawJustificationRow(doc, fonts, labels, left, y, tableWidth, data, language);
    y = drawBudgetSentenceRow(doc, fonts, labels, left, y, tableWidth, data, language);

    let header = drawItemsHeader(doc, fonts, labels, left, y, gridWidths, language);
    y = header.rowY;

    const itemColumns = header.columns;
    const lines = Array.isArray(data.lignes) ? data.lignes : [];

    lines.forEach((line, index) => {
      const previewText = formatPdfText(`${index + 1}- ${sanitizeText(line?.articleService)}`.trim(), language);
      const previewHeight = Math.max(
        24,
        measureTextHeight(doc, fonts, previewText, Math.max(0, itemColumns[0] - 10), {
          fontSize: 8.8,
          align: resolveTextAlign(previewText, language, language === "ar" ? "right" : "left"),
          lineGap: 1,
        }) + 8,
      );

      if (y + previewHeight > bottomLimit - 18) {
        doc.addPage();
        y = top;
        header = drawItemsHeader(doc, fonts, labels, left, y, gridWidths, language);
        y = header.rowY;
      }

      y = drawItemRow(doc, fonts, left, y, header.columns, line, index, language);
    });

    const trailingReserve = 22 + 28 + administrationBlockHeight();
    if (y + trailingReserve > bottomLimit) {
      doc.addPage();
      y = top;
    }

    y = drawTotalRow(doc, fonts, labels, left, y, gridWidths, data.totalGeneral, language);
    y = drawAttachmentsRow(doc, fonts, labels, left, y, gridWidths, data, language);

    if (y + administrationBlockHeight() > bottomLimit) {
      doc.addPage();
      y = top;
    }

    drawAdministrationBlocks(doc, fonts, labels, left, y, gridWidths, language);
    doc.end();
  });
}

