function drawSimpleRow(doc, fonts, x, y, gridWidths, spans, height, texts, language, options = {}) {
  const widths = widthsFromSpans(gridWidths, spans);
  const aligns = options.aligns || [];
  const weights = options.weights || [];
  const fontSizes = options.fontSizes || [];
  const colors = options.colors || [];
  const paddings = options.paddings || [];
  const valigs = options.valigns || [];
  const lineGaps = options.lineGaps || [];

  let cursorX = x;
  widths.forEach((width, index) => {
    strokeCell(doc, cursorX, y, width, height);
    drawTextInCell(doc, fonts, texts[index] || "", cursorX, y, width, height, language, {
      align: aligns[index] || "center",
      weight: weights[index] || "regular",
      fontSize: fontSizes[index] || 8.7,
      color: colors[index] || "#111827",
      padding: paddings[index] || 5,
      valign: valigs[index] || "middle",
      lineGap: lineGaps[index] || 0,
    });
    cursorX += width;
  });

  return y + height;
}

function drawIdentityRow(doc, fonts, labels, x, y, gridWidths, data, language) {
  const spans = [3, 13, 2];
  const widths = widthsFromSpans(gridWidths, spans);
  const values = [
    data.demandeurNomPrenom,
    data.directionServiceLabo,
    formatDate(data.dateDemande, language),
  ];
  const maxValueHeight = Math.max(
    ...values.map((value, index) =>
      measureTextHeight(doc, fonts, formatPdfText(value, language), Math.max(0, widths[index] - 10), {
        fontSize: 8.9,
        align: resolveTextAlign(value, language, language === "ar" ? "right" : "left"),
      }),
    ),
  );
  const rowHeight = Math.max(38, 18 + maxValueHeight + 7);

  let cursorX = x;
  const titles = [labels.fullName, labels.department, labels.date];
  widths.forEach((width, index) => {
    drawFieldCell(doc, fonts, titles[index], values[index], cursorX, y, width, rowHeight, language);
    cursorX += width;
  });

  return y + rowHeight;
}

