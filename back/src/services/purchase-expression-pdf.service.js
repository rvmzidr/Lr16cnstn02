function sanitizeText(value) {
  return String(value || "").replace(/\r\n/g, "\n").trim();
}

function normalizeLanguage(value) {
  return ["fr", "en", "ar"].includes(value) ? value : "fr";
}

function toFiniteNumber(value, fallback = 0) {
  const numeric = Number(value);
  return Number.isFinite(numeric) ? numeric : fallback;
}

function isArabicText(value) {
  return /[\u0600-\u06FF]/.test(String(value || ""));
}

function reverseRtlText(value) {
  return String(value || "")
    .split("\n")
    .map((line) => Array.from(line).reverse().join(""))
    .join("\n");
}

function formatPdfText(value, language) {
  const text = sanitizeText(value);
  if (!text) {
    return "";
  }

  if (language === "ar" || isArabicText(text)) {
    return reverseRtlText(text);
  }

  return text;
}

function resolveTextAlign(text, language, fallback = "left") {
  return language === "ar" || isArabicText(text) ? "right" : fallback;
}

function formatDate(value, language) {
  if (!value) {
    return "";
  }

  const date = value instanceof Date ? value : new Date(value);
  if (!Number.isFinite(date.getTime())) {
    return "";
  }

  const locale =
    language === "ar" ? "ar-TN" : language === "en" ? "en-US" : "fr-FR";

  return new Intl.DateTimeFormat(locale, {
    day: "2-digit",
    month: "2-digit",
    year: "numeric",
  }).format(date);
}

function formatMoney(value, language) {
  const numeric = toFiniteNumber(value, 0);
  const locale =
    language === "ar" ? "ar-TN" : language === "en" ? "en-US" : "fr-TN";

  return new Intl.NumberFormat(locale, {
    minimumFractionDigits: 3,
    maximumFractionDigits: 3,
  }).format(numeric);
}

function registerFonts(doc) {
  const regular = FONT_PATHS.regular.find((candidate) => fs.existsSync(candidate));
  const bold = FONT_PATHS.bold.find((candidate) => fs.existsSync(candidate));

  if (regular) {
    doc.registerFont("LRSans", regular);
  }
  if (bold) {
    doc.registerFont("LRSans-Bold", bold);
  }

  return {
    regular: regular ? "LRSans" : "Helvetica",
    bold: bold ? "LRSans-Bold" : "Helvetica-Bold",
  };
}

function useFont(doc, fonts, weight = "regular") {
  return doc.font(weight === "bold" ? fonts.bold : fonts.regular);
}

function strokeCell(doc, x, y, width, height) {
  doc.save();
  doc.lineWidth(0.75).strokeColor("#111827").rect(x, y, width, height).stroke();
  doc.restore();
}

function drawTextBlock(doc, fonts, text, x, y, width, options = {}) {
  const {
    weight = "regular",
    fontSize = 9,
    align = "left",
    color = "#111827",
    lineGap = 0,
    height,
  } = options;

  useFont(doc, fonts, weight).fillColor(color).fontSize(fontSize).text(text || "", x, y, {
    width,
    align,
    lineGap,
    ...(height ? { height } : {}),
  });
}

function measureTextHeight(doc, fonts, text, width, options = {}) {
  const { weight = "regular", fontSize = 9, align = "left", lineGap = 0 } = options;
  useFont(doc, fonts, weight).fontSize(fontSize);
  return doc.heightOfString(text || " ", {
    width,
    align,
    lineGap,
  });
}

function buildGridWidths(maxWidth) {
  const scale = maxWidth / OFFICIAL_TABLE_WIDTH;
  return GRID_COLUMNS_DXA.map((value) => (value / 20) * scale);
}

function widthsFromSpans(gridWidths, spans) {
  let index = 0;
  return spans.map((span) => {
    const width = gridWidths.slice(index, index + span).reduce((sum, value) => sum + value, 0);
    index += span;
    return width;
  });
}

function drawTextInCell(doc, fonts, text, x, y, width, height, language, options = {}) {
  const {
    weight = "regular",
    fontSize = 8.8,
    align = "center",
    color = "#111827",
    lineGap = 0,
    padding = 5,
    valign = "middle",
  } = options;

  const prepared = formatPdfText(text, language);
  if (!prepared) {
    return;
  }

  const resolvedAlign =
    align === "auto" ? resolveTextAlign(prepared, language, language === "ar" ? "right" : "left") : align;
  const availableWidth = Math.max(0, width - padding * 2);
  const textHeight = measureTextHeight(doc, fonts, prepared, availableWidth, {
    weight,
    fontSize,
    align: resolvedAlign,
    lineGap,
  });
  const textY = valign === "middle" ? y + Math.max(4, (height - textHeight) / 2) : y + padding;

  drawTextBlock(doc, fonts, prepared, x + padding, textY, availableWidth, {
    weight,
    fontSize,
    align: resolvedAlign,
    color,
    lineGap,
    height: Math.max(0, height - (textY - y) - padding),
  });
}

function drawFieldCell(doc, fonts, label, value, x, y, width, height, language) {
  strokeCell(doc, x, y, width, height);

  drawTextInCell(doc, fonts, label, x, y + 2, width, 12, language, {
    weight: "bold",
    fontSize: 8.6,
    align: "center",
    padding: 4,
    valign: "middle",
  });

  const preparedValue = formatPdfText(value, language);
  drawTextInCell(doc, fonts, preparedValue, x, y + 14, width, height - 16, language, {
    fontSize: 8.9,
    align: resolveTextAlign(preparedValue, language, language === "ar" ? "right" : "left"),
    padding: 5,
    valign: "top",
  });
}

function drawVerticalSeparator(doc, x, y, height) {
  doc.save();
  doc
    .lineWidth(0.55)
    .strokeColor("#111827")
    .moveTo(x, y)
    .lineTo(x, y + height)
    .stroke();
  doc.restore();
}

function drawCaptionInArea(doc, fonts, label, x, y, width, language, options = {}) {
  const {
    weight = "regular",
    fontSize = 8.1,
    align = "center",
    padding = 4,
    maxHeight = 14,
    lineGap = 0.5,
  } = options;
  const prepared = formatPdfText(label, language);
  if (!prepared) {
    return;
  }

  drawTextBlock(doc, fonts, prepared, x + padding, y + 3, Math.max(0, width - padding * 2), {
    weight,
    fontSize,
    align: align === "auto" ? resolveTextAlign(prepared, language, language === "ar" ? "right" : "left") : align,
    lineGap,
    height: maxHeight,
  });
}

function drawBlankCaptionCell(doc, fonts, label, x, y, width, height, language, options = {}) {
  strokeCell(doc, x, y, width, height);
  drawCaptionInArea(doc, fonts, label, x, y, width, language, options);
}

function measureInlineTextWidth(doc, fonts, text, language, options = {}) {
  const { weight = "regular", fontSize = 8.2 } = options;
  const prepared = formatPdfText(text, language);
  useFont(doc, fonts, weight).fontSize(fontSize);
  return doc.widthOfString(prepared || "");
}

function drawCheckboxSquare(doc, x, y, size = 8) {
  doc.save();
  doc.lineWidth(0.7).strokeColor("#111827").rect(x, y, size, size).stroke();
  doc.restore();
}

function drawCheckboxOptions(doc, fonts, labels, x, y, width, language, options = {}) {
  const { fontSize = 8.1, boxSize = 8, itemGap = 16, weight = "regular" } = options;
  const prepared = labels.map((label) => formatPdfText(label, language));
  useFont(doc, fonts, weight).fontSize(fontSize);
  const optionWidths = prepared.map((label) => boxSize + 4 + doc.widthOfString(label || ""));
  const totalWidth =
    optionWidths.reduce((sum, value) => sum + value, 0) + itemGap * Math.max(0, labels.length - 1);
  let cursorX = x + Math.max(4, (width - totalWidth) / 2);

  prepared.forEach((label, index) => {
    drawCheckboxSquare(doc, cursorX, y, boxSize);
    drawTextBlock(doc, fonts, label, cursorX + boxSize + 4, y - 1, optionWidths[index] - boxSize - 4 + 2, {
      weight,
      fontSize,
      align: "left",
      height: boxSize + 4,
    });
    cursorX += optionWidths[index] + itemGap;
  });
}

function drawSplitEntryCell(doc, fonts, labels, x, y, width, height, language, options = {}) {
  const ratios = Array.isArray(options.ratios) && options.ratios.length ? options.ratios : [0.42, 0.58];
  strokeCell(doc, x, y, width, height);

  let cursorX = x;
  labels.forEach((label, index) => {
    const currentWidth =
      index === labels.length - 1
        ? x + width - cursorX
        : Math.round(width * (ratios[index] || 0));

    if (index > 0) {
      drawVerticalSeparator(doc, cursorX, y, height);
    }

    drawCaptionInArea(doc, fonts, label, cursorX, y, currentWidth, language, {
      weight: index === 0 ? "regular" : "regular",
      fontSize: 8.05,
      align: "center",
    });
    cursorX += currentWidth;
  });
}

function drawChoiceCell(doc, fonts, title, optionLabels, x, y, width, height, language) {
  strokeCell(doc, x, y, width, height);
  const titleWidth = Math.round(width * 0.46);
  drawVerticalSeparator(doc, x + titleWidth, y, height);

  drawTextInCell(doc, fonts, title, x, y, titleWidth, height, language, {
    fontSize: 8.05,
    align: "center",
    padding: 4,
    valign: "middle",
  });

  const optionsY = y + Math.max(6, (height - 8) / 2);
  drawCheckboxOptions(doc, fonts, optionLabels, x + titleWidth + 2, optionsY, width - titleWidth - 4, language, {
    fontSize: 8.1,
    boxSize: 8,
    itemGap: 14,
  });
}

function drawApprovalChoiceCell(doc, fonts, title, yesLabel, noLabel, signatureLabel, x, y, width, height, language) {
  strokeCell(doc, x, y, width, height);
  const titleWidth = Math.round(width * 0.37);
  const optionsWidth = Math.round(width * 0.29);
  const signatureX = x + titleWidth + optionsWidth;
  const signatureWidth = x + width - signatureX;

  drawVerticalSeparator(doc, x + titleWidth, y, height);
  drawVerticalSeparator(doc, signatureX, y, height);

  drawTextInCell(doc, fonts, title, x, y, titleWidth, height, language, {
    fontSize: 8.0,
    align: "center",
    padding: 4,
    valign: "middle",
  });

  drawCheckboxOptions(doc, fonts, [yesLabel, noLabel], x + titleWidth + 3, y + Math.max(8, (height - 8) / 2), optionsWidth - 6, language, {
    fontSize: 8.0,
    boxSize: 8,
    itemGap: 12,
  });

  drawCaptionInArea(doc, fonts, signatureLabel, signatureX, y, signatureWidth, language, {
    fontSize: 8.0,
    align: "center",
  });
}

